package ex_010_ForLoops;

public class Lab034_ForLoop {
    public static void main(String[] args) {
        /*
         For Loop
         Help you to repeat a block of code.
         Initialization -> Condition -> Updation(Incre/decre)
         ICU
        */
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        for (int krishnan = 0; krishnan < 10; krishnan++) {
            System.out.println(krishnan);
        }
    }
}
