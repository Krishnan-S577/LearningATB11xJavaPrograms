package ex_010_ForLoops;

public class Lab037_For_If_Loop {
    public static void main(String[] args) {
        for(int krishnan= 0;krishnan<18;krishnan++){
            if(krishnan>15){
                System.out.println("Gift");
            }else{
                System.out.println("No Gift");
            }
        }
    }
}
